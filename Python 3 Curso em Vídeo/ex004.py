x = input('Write something: ')
print('The type of this value is', type(x))
print('Is a alphabetical?', x.isalpha())
print('Is a number?', x.isnumeric())
print('is a alphanumeric?', x.isalnum())
print('Are there only spaces?', x.isspace()) #Só tem espaços?
print('Is it  in uppercase?', x.isupper()) #Está em maiúscula?
print('Is it in lowercase?', x.islower()) #Está em minúscula?
print('is it capitalized?', x.istitle()) #Está capitalizada? (Em maiúscula a primeira letra)












